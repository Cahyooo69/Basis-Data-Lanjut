        // TODO add your handling code here:
        PreparedStatement preparedStatement;
        
        try {
            String sql = "INSERT INTO pelanggan (id_pelanggan, nama, alamat, telpon) VALUES (?, ?, ?, ?)";

            preparedStatement = conn.prepareStatement(sql);

            preparedStatement.setString(1, TFid.getText());
            preparedStatement.setString(2, TFnama.getText());
            preparedStatement.setString(3, TFalamat.getText());
            preparedStatement.setString(4, TFtelpon.getText());

            preparedStatement.executeUpdate();

            String sql1 = "INSERT INTO transaksi (id_transaksi, total_harga, id_pelanggan, id_paket) VALUES (?, ?, ?, ?)";

            PreparedStatement preparedStatement1;
            preparedStatement1 = conn.prepareStatement(sql1);
            preparedStatement1.setString(1, TFantrian.getText());
            preparedStatement1.setString(2, harga.getText());
            preparedStatement1.setString(3, TFid.getText());
            preparedStatement1.setString(4, this.selectedIdPaket);

            preparedStatement1.executeUpdate();

            JOptionPane.showMessageDialog(this, "Lanjut ke pembayaran");
            this.getAllData();
            this.refreshModel();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "data gagal ditambahkan", "Error", JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
        Pembayaran frame1 = new Pembayaran();
        frame1.setVisible(true);


