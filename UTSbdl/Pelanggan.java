    public final void getAllData() {
        try {
            dataList.clear();
            String sql = "SELECT p.nama, t.id_transaksi "
                    + "FROM pelanggan p "
                    + "LEFT JOIN transaksi t ON p.id_pelanggan = t.id_pelanggan";
            conn = DriverManager.getConnection(koneksi, user, password);
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.executeQuery();
            final ResultSet data = ps.getResultSet();
            while (data.next()) {
                final String[] row = new String[]{
                    data.getString("nama"),
                    data.getString("id_transaksi")};
                dataList.add(row);
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

    /**
     * Creates new form Uts
     */
    public final void refreshModel() {
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(columns);
        for (String[] data : dataList) {
            model.addRow(data);
        }
        Table.setModel(model);
    }

