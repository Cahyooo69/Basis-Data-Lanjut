    private void insertBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertBActionPerformed
        // TODO add your handling code here:
        String id_paket = TFid.getText();
        String nama_paket = TFnama.getText();
        String harga_paket = TFharga.getText();
        
        if (id_paket.trim().isEmpty() && nama_paket.trim().isEmpty() && harga_paket.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan data anda");

        } else if (id_paket.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan id_paket");
        } else if (nama_paket.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan nama_paket");
        } else if (harga_paket.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "masukan harga_paket");
        }
        else {
            PreparedStatement preparedStatement;
            try {
                String sql = "INSERT INTO paket_laundry (id_paket, nama_paket, harga_paket) VALUES (?, ?, ?)";

                preparedStatement = conn.prepareStatement(sql);

                preparedStatement.setString(1, TFid.getText());
                preparedStatement.setString(2, TFnama.getText());
                preparedStatement.setString(3, TFharga.getText());

                preparedStatement.executeUpdate();
                this.getAllData();
                this.refreshModel();
                JOptionPane.showMessageDialog(this, "data berhasil ditambahkan");

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal ditambahkan", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_insertBActionPerformed

